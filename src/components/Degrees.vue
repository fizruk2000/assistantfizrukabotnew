  onTimeInput(index) {
    const event = this.events[index];
    const seconds = this.parseTimeToSeconds(event.resultInput.trim());
    if (seconds === null) {
      event.points = 0;
      event.result = 0;
    } else {
      event.result = seconds;
      this.calculatePoints(index);
    }
  },
  calculatePoints(index) {
    const event = this.events[index];
    const result = event.result;
    const resultArray = event.resultsArray;
    const isTimeBased = event.isTimeBased;

    if (result <= 0 || resultArray.length === 0) {
      event.points = 0;
      return;
    }

    let low = 0;
    let high = resultArray.length - 1;
    let points = 0;

    if (isTimeBased) {
      // Для времени — ищем минимальный индекс с resultArray[mid].result >= result
      while (low <= high) {
        const mid = Math.floor((low + high) / 2);
        if (resultArray[mid].result >= result) {
          points = resultArray[mid].points;
          high = mid - 1;
        } else {
          low = mid + 1;
        }
      }
    } else {
      // Для прыжков и метаний — ищем максимальный индекс с resultArray[mid].result <= result
      while (low <= high) {
        const mid = Math.floor((low + high) / 2);
        if (resultArray[mid].result <= result) {
          points = resultArray[mid].points;
          low = mid + 1;
        } else {
          high = mid - 1;
        }
      }
    }

    event.points = points;
  },
},
computed: {
  totalPoints() {
    return this.events.reduce((sum, event) => sum + event.points, 0);
  },
},
